package com.paladin.demo.dynamic.model;

import java.util.Date;

public class DynamicDemo {
	private String name;
	private Date time;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
}
